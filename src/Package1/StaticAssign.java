package Package1;
public class StaticAssign 
{
	 static void add ()
	{
		int a=100;
		int b=200;
		System.out.println(a+b);		
	}
	 static void sub()
		{
			int a=500;
			int b=200;
			System.out.println(a-b);			
		}
	 static void multiply()
		{
			int a=10;
			int b=15;
			System.out.println(a*b);
		}
	 static void divide()
		{
			int a=10;
			int b=5;
			System.out.println(a/b);		
		}
	 static void modulos()
		{
			int a=50;
			int b=15;
			System.out.println(a%b);			
		}
	 
	public static void main(String[] args) 
	
		
		{
		add();
		sub();
		multiply();
		divide();
		modulos();
	}
		

}
